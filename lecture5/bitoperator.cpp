#include<iostream>
using namespace std;
int main(){
    int a=4,b=6,i=7;
    cout<<"a&b: "<<(a&b)<<endl;
    cout<<"a|b: "<<(a|b)<<endl;
    cout<<"~a "<<(~a)<<endl;
    cout<<"a^b: "<<(a^b)<<endl;

    cout<<(17>>2)<<endl;
    cout<<(19<<2)<<endl;

    cout<<(++i)<<endl;
    cout<<(i++)<<endl;
    cout<<(i--)<<endl;
    cout<<(--i)<<endl;

}